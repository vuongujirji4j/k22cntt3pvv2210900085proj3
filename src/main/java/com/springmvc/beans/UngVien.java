package com.springmvc.beans;

public class UngVien {
    private int maUngVien;
    private String hoTen;
    private String viTriUngTuyen;
    private String trangThai;
    private String ketQuaPhongVan;

    // Getters & Setters
    public int getMaUngVien() { return maUngVien; }
    public void setMaUngVien(int maUngVien) { this.maUngVien = maUngVien; }

    public String getHoTen() { return hoTen; }
    public void setHoTen(String hoTen) { this.hoTen = hoTen; }

    public String getViTriUngTuyen() { return viTriUngTuyen; }
    public void setViTriUngTuyen(String viTriUngTuyen) { this.viTriUngTuyen = viTriUngTuyen; }

    public String getTrangThai() { return trangThai; }
    public void setTrangThai(String trangThai) { this.trangThai = trangThai; }

    public String getKetQuaPhongVan() { return ketQuaPhongVan; }
    public void setKetQuaPhongVan(String ketQuaPhongVan) { this.ketQuaPhongVan = ketQuaPhongVan; }
}
